// Current language state
let currentLanguage = 'english';

// Language management functions
const LanguageManager = {
    // Initialize language system
    init() {
        this.loadSavedLanguage();
        this.setupLanguageSelector();
        this.translatePage();
    },

    // Load saved language from localStorage
    loadSavedLanguage() {
        const saved = localStorage.getItem('swar-vyapaar-language');
        if (saved && translations[saved]) {
            currentLanguage = saved;
            const selector = document.getElementById('languageSelector');
            if (selector) {
                selector.value = currentLanguage;
            }
        }
    },

    // Setup language selector event listener
    setupLanguageSelector() {
        const selector = document.getElementById('languageSelector');
        if (selector) {
            selector.addEventListener('change', (e) => {
                this.changeLanguage(e.target.value);
            });
        }
    },

    // Change language
    changeLanguage(language) {
        if (translations[language]) {
            currentLanguage = language;
            localStorage.setItem('swar-vyapaar-language', language);
            this.translatePage();
            
            // Update voice recognition language
            if (window.VoiceManager) {
                window.VoiceManager.updateLanguage(language);
            }
            
            // Trigger custom event for other components
            window.dispatchEvent(new CustomEvent('languageChanged', { 
                detail: { language } 
            }));
        }
    },

    // Translate entire page
    translatePage() {
        const elements = document.querySelectorAll('[data-translate]');
        elements.forEach(element => {
            const key = element.getAttribute('data-translate');
            const translation = this.t(key);
            if (translation) {
                element.textContent = translation;
            }
        });

        // Handle placeholder translations
        const placeholderElements = document.querySelectorAll('[data-translate-placeholder]');
        placeholderElements.forEach(element => {
            const key = element.getAttribute('data-translate-placeholder');
            const translation = this.t(key);
            if (translation) {
                element.placeholder = translation;
            }
        });

        // Update document title
        document.title = `${this.t('appName')} | ${this.t('tagline')}`;
    },

    // Get translation for key
    t(key) {
        return translations[currentLanguage]?.[key] || translations.english[key] || key;
    },

    // Get current language
    getCurrentLanguage() {
        return currentLanguage;
    },

    // Get language code for Web Speech API
    getLanguageCode(language = currentLanguage) {
        const languageCodes = {
            english: 'en-IN',
            hindi: 'hi-IN',
            telugu: 'te-IN',
            tamil: 'ta-IN',
            kannada: 'kn-IN',
            malayalam: 'ml-IN',
            marathi: 'mr-IN',
            odia: 'or-IN',
            tulu: 'en-IN' // Fallback to English for Tulu
        };
        return languageCodes[language] || 'en-IN';
    },

    // Get all available languages
    getAvailableLanguages() {
        return Object.keys(translations).map(lang => ({
            code: lang,
            name: translations[lang].appName.split(' – ')[0],
            nativeName: this.getNativeName(lang)
        }));
    },

    // Get native name for language
    getNativeName(lang) {
        const nativeNames = {
            english: 'English',
            hindi: 'हिंदी',
            telugu: 'తెలుగు',
            tamil: 'தமிழ்',
            kannada: 'ಕನ್ನಡ',
            malayalam: 'മലയാളം',
            marathi: 'मराठी',
            odia: 'ଓଡ଼ିଆ',
            tulu: 'ತುಳು'
        };
        return nativeNames[lang] || lang;
    }
};

// Export for global use
window.LanguageManager = LanguageManager;
window.translations = translations;